#!/usr/bin/env node
/**
 * Generates CRON_SECRET and prints Render setup instructions.
 * Run: node scripts/setup-cron-secret.js
 */
const crypto = require('crypto');
const fs = require('fs');
const path = require('path');

const secret = crypto.randomBytes(32).toString('hex');
const envPath = path.join(__dirname, '..', '.env');
let envContent = '';
if (fs.existsSync(envPath)) {
  envContent = fs.readFileSync(envPath, 'utf8');
  if (envContent.includes('CRON_SECRET=')) {
    envContent = envContent.replace(/CRON_SECRET=.*/m, `CRON_SECRET=${secret}`);
  } else {
    envContent += `\n# Scheduled messages cron\nCRON_SECRET=${secret}\n`;
  }
} else {
  envContent = `# Generated by setup-cron-secret.js\nCRON_SECRET=${secret}\n`;
}
fs.writeFileSync(envPath, envContent.trim() + '\n');

const appUrl = 'bodybank-fit.onrender.com';
const cronUrl = `https://${appUrl}/api/cron/process-scheduled-messages?secret=${secret}`;

console.log('\n=== CRON_SECRET generated and saved to .env ===\n');
console.log('1. RENDER DASHBOARD - Web service (bodybank-fit):');
console.log('   Add env var: CRON_SECRET = ' + secret);
console.log('\n2. RENDER DASHBOARD - Cron service (bodybank-scheduled-messages):');
console.log('   Add env vars:');
console.log('   - CRON_SECRET = ' + secret);
console.log('   - APP_URL = ' + appUrl + '  (optional, this is default)');
console.log('\n3. If NOT using Render Cron, use cron-job.org with this URL:');
console.log('   ' + cronUrl);
console.log('   Schedule: every 5 minutes\n');
